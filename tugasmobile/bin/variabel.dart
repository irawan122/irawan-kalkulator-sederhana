void main() {
  // Variabel string
  String nama = "irawan";

  // Variabel integer
  int umur = 25;

  // Variabel double
  double tinggi = 175.5;

  // Variabel boolean
  bool sudahMenikah = false;

  // Menampilkan nilai variabel
  print("Nama: $nama");
  print("Umur: $umur");
  print("Tinggi: $tinggi");
  print("Sudah Menikah: $sudahMenikah");
}